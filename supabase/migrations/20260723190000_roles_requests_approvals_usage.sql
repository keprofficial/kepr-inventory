-- Two-role workflow: inventory admin and apartment user.

create table if not exists public.inventory_users (
  user_id uuid primary key references auth.users(id) on delete cascade,
  role text not null check (role in ('inventory_admin','apartment')),
  apartment_id bigint references public.inventory_apartments(id) on delete cascade,
  display_name text not null default '',
  created_at timestamptz not null default now(),
  check (
    (role='inventory_admin' and apartment_id is null) or
    (role='apartment' and apartment_id is not null)
  )
);

create table if not exists public.inventory_requests (
  id bigint generated by default as identity primary key,
  reference text not null unique default (
    'REQ-' || to_char(current_date,'YYYYMMDD') || '-'
    || upper(substr(md5(gen_random_uuid()::text),1,6))
  ),
  apartment_id bigint not null references public.inventory_apartments(id),
  requested_by uuid not null references auth.users(id),
  status text not null default 'pending'
    check (status in ('pending','approved','rejected')),
  note text not null default '',
  admin_note text not null default '',
  requested_at timestamptz not null default now(),
  reviewed_at timestamptz,
  reviewed_by uuid references auth.users(id)
);

create table if not exists public.inventory_request_lines (
  id bigint generated by default as identity primary key,
  request_id bigint not null references public.inventory_requests(id)
    on delete cascade,
  product_id bigint not null references public.inventory_products(id),
  quantity numeric(14,3) not null check (quantity > 0),
  unique(request_id,product_id)
);

create table if not exists public.inventory_usage (
  id bigint generated by default as identity primary key,
  apartment_id bigint not null references public.inventory_apartments(id),
  product_id bigint not null references public.inventory_products(id),
  quantity numeric(14,3) not null check (quantity > 0),
  usage_date date not null default current_date,
  note text not null default '',
  recorded_by uuid not null references auth.users(id),
  created_at timestamptz not null default now()
);
create index if not exists inventory_usage_month_idx
  on public.inventory_usage(apartment_id,usage_date desc);

alter table public.inventory_users enable row level security;
alter table public.inventory_requests enable row level security;
alter table public.inventory_request_lines enable row level security;
alter table public.inventory_usage enable row level security;

create or replace function public.inventory_is_admin()
returns boolean language sql stable security definer set search_path=public as $$
  select exists(select 1 from inventory_users
    where user_id=auth.uid() and role='inventory_admin')
$$;

drop policy if exists "users read self or admin" on public.inventory_users;
create policy "users read self or admin" on public.inventory_users for select
  to authenticated using (user_id=auth.uid() or inventory_is_admin());
drop policy if exists "requests read own apartment or admin" on public.inventory_requests;
create policy "requests read own apartment or admin" on public.inventory_requests
  for select to authenticated using (
    inventory_is_admin() or apartment_id=(
      select apartment_id from inventory_users where user_id=auth.uid()
    )
  );
drop policy if exists "request lines follow request access" on public.inventory_request_lines;
create policy "request lines follow request access" on public.inventory_request_lines
  for select to authenticated using (
    exists(select 1 from inventory_requests r where r.id=request_id)
  );
drop policy if exists "usage read own apartment or admin" on public.inventory_usage;
create policy "usage read own apartment or admin" on public.inventory_usage
  for select to authenticated using (
    inventory_is_admin() or apartment_id=(
      select apartment_id from inventory_users where user_id=auth.uid()
    )
  );

