drop policy if exists "inventory authenticated products" on public.inventory_products;
create policy "inventory authenticated products" on public.inventory_products
  for all to authenticated using (true) with check (true);
drop policy if exists "inventory authenticated apartments" on public.inventory_apartments;
create policy "inventory authenticated apartments" on public.inventory_apartments
  for all to authenticated using (true) with check (true);
drop policy if exists "inventory authenticated stock" on public.inventory_stock_levels;
create policy "inventory authenticated stock" on public.inventory_stock_levels
  for all to authenticated using (true) with check (true);
drop policy if exists "inventory authenticated movements" on public.inventory_stock_movements;
create policy "inventory authenticated movements" on public.inventory_stock_movements
  for all to authenticated using (true) with check (true);

